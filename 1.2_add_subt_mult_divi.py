num1 = int(input("Enter a first number: "))
num2 = int(input("Enter a second number: "))

add = num1 + num2
print(f"Addition of {num1} and {num2} is: {add}")

subt = num1 - num2
print(f"Subtraction of {num1} and {num2} is: {subt}")

multi = num1 * num2
print(f"Multiplication of {num1} and {num2} is: {multi}")

divis = num1 / num2
print(f"Division of {num1} and {num2} is: {divis}")

moduls = num1 % num2
print(f"Modulus of {num1} and {num2} is: {moduls}")